var obj_hover = document.getElementById('menu-hover');
obj_hover.addEventListener('mouseenter', function(){
    this.style = "background-color: #2D8ECE";
})
obj_hover.addEventListener('mouseleave' , function(){
    this.style = 'background-color: red' ;
})
